def main():
    """ Main Program """
 
    # Call this function so the Pygame library can initialize itself
    pygame.init()
 
    # Create an 800x600 sized screen
    screen = pygame.display.set_mode([SCREEN_WIDTH, SCREEN_HEIGHT])
 
    # Set the title of the window
    pygame.display.set_caption('DRACU-RIOT [Prototype]')

    # Group Lists
    all_sprites_list = pygame.sprite.Group()
    people_list = pygame.sprite.Group()

    # Create people in random position
    for i in range(5):
        # This represents a block
        people = People()

        # Set a random location for the block
        people.rect.x = random.randrange(20, SCREEN_WIDTH-200)
        people.rect.y = random.randrange(100, SCREEN_HEIGHT-200)
        #people.rect.x = SCREEN_WIDTH-200
        #people.rect.y = SCREEN_HEIGHT-200

        # Add the block to the list of objects
        people_list.add(people)
        all_sprites_list.add(people)
 
    # Create the player paddle object
    player = Player(50, 200)
    all_sprites_list.add(player)
 
    rooms = []
 
    room = Room1()
    rooms.append(room)
 
    room = Room2()
    rooms.append(room)
 
    room = Room3()
    rooms.append(room)
 
    current_room_no = 0
    current_room = rooms[current_room_no]
 
    clock = pygame.time.Clock()
 
    done = False

    score = 0
 
    while not done:
 
        # --- Event Processing ---
 
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                done = True
 
            if event.type == pygame.KEYDOWN:
                if event.key == pygame.K_LEFT:
                    player.changespeed(-5, 0)
                if event.key == pygame.K_RIGHT:
                    player.changespeed(5, 0)
                if event.key == pygame.K_UP:
                    player.changespeed(0, -5)
                if event.key == pygame.K_DOWN:
                    player.changespeed(0, 5)
 
            if event.type == pygame.KEYUP:
                if event.key == pygame.K_LEFT:
                    player.changespeed(5, 0)
                if event.key == pygame.K_RIGHT:
                    player.changespeed(-5, 0)
                if event.key == pygame.K_UP:
                    player.changespeed(0, 5)
                if event.key == pygame.K_DOWN:
                    player.changespeed(0, -5)
 
        # --- Game Logic ---
 
        player.move(current_room.wall_list)
 
        if player.rect.x < -15:
            if current_room_no == 0:
                current_room_no = 2
                current_room = rooms[current_room_no]
                player.rect.x = 790
            elif current_room_no == 2:
                current_room_no = 1
                current_room = rooms[current_room_no]
                player.rect.x = 790
            else:
                current_room_no = 0
                current_room = rooms[current_room_no]
                player.rect.x = 790
 
        if player.rect.x > 801:
            if current_room_no == 0:
                current_room_no = 1
                current_room = rooms[current_room_no]
                player.rect.x = 0
            elif current_room_no == 1:
                current_room_no = 2
                current_room = rooms[current_room_no]
                player.rect.x = 0
            else:
                current_room_no = 0
                current_room = rooms[current_room_no]
                player.rect.x = 0
                
        # --- Collision Handler---
        people_hit_list = pygame.sprite.spritecollide(player, people_list, True)

        # Check the list of collisions.
        for person in people_hit_list:
            score += 1
            print(score)
 
        # --- Drawing ---
        screen.blit(BG, (0,0))
        
        all_sprites_list.draw(screen)
        current_room.wall_list.draw(screen)
 
        pygame.display.flip()
 
        clock.tick(60)
 
    pygame.quit()
 
if __name__ == "__main__":
    main()
